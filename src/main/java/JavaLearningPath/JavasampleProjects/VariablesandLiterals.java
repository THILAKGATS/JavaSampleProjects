package JavaLearningPath.JavasampleProjects;

public class VariablesandLiterals 
{
	public static void main(String args[])
	{
		
		int ticketPrice = 800; // variable declaration 
		System.out.println(ticketPrice);
		ticketPrice = 1000;
		System.out.println("The ticket price is" + ticketPrice);
		/******literals*******/
		// the value is called as literals
		int a = 10; //numerical literal
		boolean value = true; // boolean literal 
		char c = '4';// char literals
		//hexadecimal
		int num = 0x10;
		float f = 187788;
		long len = 802214880;
		System.out.println(len);
		System.out.println(f);
		
		System.out.println(num);
		
	}
	
}
